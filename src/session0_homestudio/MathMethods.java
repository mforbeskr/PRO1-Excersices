package session0_homestudio;

public class MathMethods
{
  public static void main(String[] args)
  {

    System.out.println("PI: " + Math.PI);
    System.out.println("Eulers: " + Math.E);

    double result;

    result = Math.sqrt(4);
    result = Math.pow(2, 4);
    result = Math.max(1, 44);
    result = Math.min(1, 44);
    result = Math.abs(-44);
    result = Math.random();
    result = Math.round(3.14);
    result = Math.ceil(3.44);
    result = Math.floor(3.51);


    System.out.println(result);



  }
}
